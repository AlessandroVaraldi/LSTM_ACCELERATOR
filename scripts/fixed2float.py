import struct

def fixed_to_float_hex(hex_value):
    # Interpret the hex value as a signed integer with 32 bits
    print(hex_value)
    int_value = int(hex_value, 16)
    # If the highest bit is set (negative number in two's complement)
    if int_value & 0x80000000:
        int_value -= 0x100000000

    # Convert fixed point 8.24 to float
    float_value = int_value / (2**24)

    # Pack the float into bytes and convert to hex
    float_hex = struct.pack('>f', float_value).hex().upper()

    return float_hex

def process_rom_entries(rom_entries):
    processed_entries = []
    
    for entry in rom_entries:
        # Split the 160-bit entry into five 32-bit values
        parts = [entry[i:i+8] for i in range(0, 40, 8)]
        
        # Convert each part and collect the new hex values
        converted_parts = [fixed_to_float_hex(part) for part in parts]
        
        # Concatenate the converted parts back into a single 160-bit value
        new_entry = ''.join(converted_parts)
        processed_entries.append(f'X"{new_entry}"')
    
    return processed_entries

